    public void cargarTrabajo(){
        try {
           
            PreparedStatement ps=null;
            ResultSet rs=null;
            Connection con=cn.conectar();
            
            int num=Integer.parseInt(""+MenuConfeccionador.jLabelIDPer.getText());
            if(jRadioButton1.isSelected()){
                estado="EN PROCESO";
            }
            if(jRadioButton2.isSelected()){
                estado="TERMINADO";
            }
            if(jRadioButton3.isSelected()){
                estado="ENTREGADO";
            }
           
            limpiarTabla(jTable1, tabla);
             
            String sql="call p_vertermcon (?,?)";
            ps=con.prepareStatement(sql);
            
           
            ps.setString(1,""+num);
            ps.setString(2,""+estado);
            rs=ps.executeQuery();
            ResultSetMetaData rsMd=(ResultSetMetaData) rs.getMetaData();
            int cantidadcolumnas=rsMd.getColumnCount();
            int [] anchos={100,150,150,130};
            for(int i=0;i<jTable1.getColumnCount();i++){
                jTable1.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
            while(rs.next()){
                Object[] filas=new  Object[cantidadcolumnas];
                for(int i=0;i<cantidadcolumnas;i++){
                    filas[i]=rs.getObject(i+1);
                }
                tabla.addRow(filas);
            }
            
        } catch (SQLException e) {
            System.err.println(e.toString());
        }
    }
    public void reported(){
        try {
            Conexion conn=new Conexion();
            Connection con=conn.conectar();
            JasperReport reporte=null;
            String path="src\\Reportes\\rTrabajos.jasper";
            Map parametro=new HashMap();
            int num;
            num=Integer.parseInt(MenuConfeccionador.jLabelIDPer.getText());
            if(jRadioButton1.isSelected()){
                estado="EN PROCESO";
            }
            if(jRadioButton2.isSelected()){
                estado="TERMINADO";
            }
            if(jRadioButton3.isSelected()){
                estado="ENTREGADO";
            }
            parametro.put("num", num);
            parametro.put("esta", estado);
            reporte=(JasperReport) JRLoader.loadObjectFromFile(path);
            JasperPrint jprint=JasperFillManager.fillReport(reporte,parametro,con);
            JasperViewer view=new JasperViewer(jprint,false);
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            view.setVisible(true);
            
        } catch (Exception e) {
            Logger.getLogger(JDialogInventario.class.getName()).log(Level.SEVERE, null, e);
        }
    }

